#include <Uart_settings.h>

/*==================================================================================================
                                             CONSTANTS
==================================================================================================*/
static UART_PROPS properties =
{
   HONEYBADGER,         // family
   660,                	// platform
   2,                   // uart_instance
   0x00000000,          // gsbi_base
   0xC170000,          	// uart_base
   0x00100000,          // clock_base
   0,		  	 		// tlmm_base 
   0,  					// rts: uart5 = (2ma, fcn x, NP)
   0,		   			// tlmm_base 
   0,  					// cts: uart5 = (2ma, fcn x, PD)
   0x03100000,		   	// tlmm_base (TLMM_SOUTH)
   (5 << 16) | 0x000D,  // rx:  uart5 = (2ma, fcn 3, PD)
   0x03900000,		   	// tlmm_base (TLMM_NORTH)
   (4 << 16) | 0x000C,  // tx:  uart5 = (2ma, fcn 3, NP)
};


/*==================================================================================================
                                             FUNCTIONS
==================================================================================================*/
UART_PROPS* uart_get_properties(void)
{
   return &properties;
}
