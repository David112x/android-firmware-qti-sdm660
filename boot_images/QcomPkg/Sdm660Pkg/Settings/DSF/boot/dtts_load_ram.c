void dtts_load_sram
(

  uint32 dtts_sram_base_addr,
  uint32 *dtts_sram_base_data,
  uint16 load_dram /*0 - decremental load 1- incremtnal load*/,
  uint16 data_length
)
{
  uint32 reg;


  /* Populate base config */
  if (dtts_sram_base_data != NULL)
  {
    if (load_dram == 0){
        for (reg = 0; reg < data_length; reg++)
        {
          out_dword(dtts_sram_base_addr+(4*reg), dtts_sram_base_data[reg]);
        }
    }
    else{
        for (reg = 0; reg < data_length; reg++)
        {
          out_dword(dtts_sram_base_addr+(4*reg), dtts_sram_base_data[0]);
        }

    }
  }
}

